package com.spring.springdi.di;

public class Student {
	
	private String number;

	public Student() {
		super();
	}

	public Student(String number) {
		super();
		this.number = number;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}
	
}
